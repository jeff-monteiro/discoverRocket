// Operadores de Comparação

// Iŕa comparar valores e retornar um Boolean como resposta à comparação

let one = 1
let two = 2

// == igual a
console.log(two == 1)
console.log(one == '1')

// != diferente de
console.log(one != two)
console.log(one != 1)
console.log(one != '1')

// === estritamente igual a
console.log(one === '1')
console.log(one === 1)

// !== estritamente diferente de
console.log(two !== '2')
console.log(two !== 2)

